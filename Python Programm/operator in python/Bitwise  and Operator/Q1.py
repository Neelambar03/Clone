# Compares each bit of two numbers. If both bits are 1, the corresponding result bit is set to 1, otherwise, it is set to 0.
a = 5  # 0101 in binary
b = 3  # 0011 in binary
print(a & b)  # 1 (0001 in binary)

# Bitwise or Operator
# Compares each bit of two numbers. If either of the bits is 1, the corresponding result bit is set to 1.
a = 5  # 0101 in binary
b = 3  # 0011 in binary
print(a | b)  # 7 (0111 in binary)
